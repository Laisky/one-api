package openrouter

import (
	"github.com/Laisky/one-api/relay/adaptor"
	"github.com/Laisky/one-api/relay/billing/ratio"
)

// openRouterModelConfig converts OpenRouter's published per-1M token prices into one-api's billing fields.
// inputPerMillion and outputPerMillion are USD prices per 1M tokens, cachedInputPerMillion is the cache-read price,
// and cacheWritePerMillion is OpenRouter's single published cache-write price, which one-api stores in CacheWrite5mRatio.
func openRouterModelConfig(inputPerMillion, outputPerMillion, cachedInputPerMillion, cacheWritePerMillion float64) adaptor.ModelConfig {
	cfg := adaptor.ModelConfig{
		Ratio:           inputPerMillion * ratio.MilliTokensUsd,
		CompletionRatio: 1,
	}
	if inputPerMillion > 0 && outputPerMillion > 0 {
		cfg.CompletionRatio = outputPerMillion / inputPerMillion
	}
	if cachedInputPerMillion != 0 {
		cfg.CachedInputRatio = cachedInputPerMillion * ratio.MilliTokensUsd
	}
	if cacheWritePerMillion != 0 {
		cfg.CacheWrite5mRatio = cacheWritePerMillion * ratio.MilliTokensUsd
	}

	return cfg
}

// openRouterModelMetadata bundles the optional metadata fields populated from
// OpenRouter's /api/v1/models endpoint.  It only carries fields that are available
// from the upstream catalog so the constants table stays compact.
type openRouterModelMetadata struct {
	ContextLength               int32
	MaxOutputTokens             int32
	InputModalities             []string
	OutputModalities            []string
	SupportedFeatures           []string
	SupportedSamplingParameters []string
	HuggingFaceID               string
	Description                 string
}

// openRouterModelConfigWithMetadata composes the pricing config produced by
// openRouterModelConfig with optional metadata sourced from OpenRouter.
// It preserves backward compatibility with the original helper signature.
func openRouterModelConfigWithMetadata(inputPerMillion, outputPerMillion, cachedInputPerMillion, cacheWritePerMillion float64, md openRouterModelMetadata) adaptor.ModelConfig {
	cfg := openRouterModelConfig(inputPerMillion, outputPerMillion, cachedInputPerMillion, cacheWritePerMillion)
	cfg.ContextLength = md.ContextLength
	cfg.MaxOutputTokens = md.MaxOutputTokens
	cfg.InputModalities = md.InputModalities
	cfg.OutputModalities = md.OutputModalities
	cfg.SupportedFeatures = md.SupportedFeatures
	cfg.SupportedSamplingParameters = md.SupportedSamplingParameters
	cfg.HuggingFaceID = md.HuggingFaceID
	cfg.Description = md.Description
	return cfg
}

